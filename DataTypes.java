public class DataTypes {
     public static void main(String[] args) {

        int num1 = 23;
        short num2 = 10;
        long num3 = 98234784;
        System.out.println("Here are the integer number \n"+ num1 + "\n" + num2 + "\n" + num3);

        // Real numbers and floating numbers 
        System.out.println("Now we will start float real numbers");

        float ft = 10.6f;
        double dbl = 10.724284;
        System.out.println("Here are the floating numbers \n" + ft + "\n" + dbl);

        //Character datatypes
        System.out.println("We will start character representation here "); 
        char c = 'a';
        System.out.println("Here are the character \n"+ c);

        //Boolean
        System.out.println("We will start boolean");
        boolean bl = true;
        System.out.println("Here is the value of boolean : "+bl);
    }
}
